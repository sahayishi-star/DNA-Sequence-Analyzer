# A k-mer is a short substring of a length, n
# A window of size n is slid across the DNA sequence, and calculates GC% at
# each position to show how GC content varies across different regions of DNA
def plot_kmers(dna,n):

  # Creates substrings of length n using sliding window
  window = [dna[i:i+n] for i in range(0,len(dna) - n+1)]
  percent_list = []
  for w in window:
    countGC2 = w.count('G') + w.count('C')
    percent2 = countGC2/len(w)*100
    percent_list.append(percent2)
  x = range(len(window)) # x-axis: window index (position along DNA sequence)
  return percent_list, x

