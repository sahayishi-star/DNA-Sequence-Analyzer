"""**Simplified Model**: Santa Lucia Model - Nearest Neighbor Model

Deals with DNA base pair stacking!

> Note!

For salt correction, I use an older formula. This formula assumed monovalent salts (1:1 charge ratio, so 1 cation and 1 anion when disolved) like NaCl.
Most Santa Lucia models use much more advanced salt corrections.





"""

## ~ TESTING DIFF DNA SEQ FOR OPEN AND CLOSE PROB.~ ##
# dna = Seq('CATAG')
# Test results: P(open) of AAAAA > P(open) of CATAG > P(open) of GGGGG

# CA/GT and TG/AC are the same duplex when viewed from opposite strands
# That's why CA and TG have the same ∆H and ∆S
# Nearest neighbor parameters in (∆H [cal/mol], ∆S [cal/mol*K] )
# Source: Source: https://www.pnas.org/doi/10.1073/pnas.95.4.1460
nn_par = {'AA': (-7900,-22.2), 'TT': (-7900,-22.2),
          'AT': (-7200 ,-20.4), 'TA': (-7200,-21.3),
          'CA': (-8500, -22.7),'TG': (-8500, -22.7),
          'GT': (-8400, -22.4),'AC': (-8400, -22.4),
          'CT': (-7800, -21.0),'AG': (-7800, -21.0),
          'GA': (-8200, -22.2),'TC': (-8200, -22.2),
          'CG': (-10600, -27.2),'GC': (-9800, -24.4),
          'GG': (-8000, -19.9),'CC': (-8000, -19.9),}

def melting_DNA(dna,n, nn_par, salt, temp):
  window = [dna[i:i+n] for i in range(0,len(dna) - n+1)]

  if len(dna) <= 14:
    print("Disclaimer: At a length of 14 base pairs or below, SantaLucia model becomes less accurate")
  melting_pt = []
  close_prob = []
  open_prob = []

  for w in window:
    G = w.count('G')
    C = w.count('C')
    A = w.count('A')
    T = w.count('T')


                  ##-- ~~ -Info on SantaLucia- ~~ --##

    # SantaLucia formula is tm = ∆H/∆S + Rln(C) - 273.15
    # Where tm is melting point, ∆H is change in enthalpy,
    # ∆S is change in entropy, R is gas constant (1.987 cal/mol*K)
    # and C is DNA strand concentration

    # Using equilibrium constants (K), we get C/4
    # If the sequence is self-complementary (can bind to itself), C/4 is not
    # needed. But for this model, we have to assume not all sequences are
    # self-complementary

    # 5' - GAATTC - 3' is self complementary.
    # If you flip it: 3' - CTTAAG - 5', its the same as the complementary
    # strand: 5' - CTTAAG - 3'


    r = 1.987

    # PCR primers are approzimately 100-500 nM
    # I'm using 300 nM as an approximate concentration
    # I'm using 10^-9 to convert nM to M
    c = 300e-9
    dh, ds = 0, 0

    # Looks at the first and last bases in the window
    for end in [w[0],w[-1]]:
      # Checks to see if the end contains G or C
      if end in ('G','C'):
        # adding penalties
        # Source: https://www.pnas.org/doi/10.1073/pnas.95.4.1460
        dh += 100; ds += -2.8
      else:
        dh += 2300; ds += 4.1


    # A/T ends have a penalty added because A/T bonds only contain 2 H-bonds
    # This means ends with A/T 'fray' more easily than ends with G/C

    for i in range(len(w) - 1):
      pair = w[i:i+2]
      h, s = nn_par.get(pair,(-8430, -22.37)) # .get(key, fallback)
      # (-8430,-22.37) is the mean of the data in the nm_par table

      # Use += to add s on top of everything in ds instead of replacing values
      # Mimics behavior of base pair stacking
      ds += s
      dh += h

    # SantaLucia equation
    tm = dh/(ds+r*np.log(c/4)) - 273.15

    # Owczarazy salt correction
    # Source: https://biopython.org/docs/1.74/api/Bio.SeqUtils.MeltingTemp.html
    # SantaLucia parameters assume NaCl concentration of 1M
    tm2 = tm + (16.6*np.log10(salt))

    # ∆G = ∆H - T∆S
    # Calculate ∆G so we can use the equilibrium constant (K) for opening prob.
    kelvins = temp + 273.15
    dg = dh - (kelvins*ds)

    # To find K with a known ∆G, use:
    # ∆G = -RTln(K)
    # Rearrange to isolate K:
    # e^(∆G/-RT) = K
    # P_open = K/(1+K)
    ## Why: DNA is Open <-> Closed, so K = Closed/Open (product/reactant)
    ## Open = K*closed
    ## Probability of open: Open/Open+Closed
    ## K*Closed/K*Closed+Closed -> Factor out Closed
    ## K/(K+1)

    K = np.exp(dg/(-r*kelvins))
    P_close = K/(K+1)
    P_open = 1 - P_close
    close_prob.append(P_close)
    open_prob.append(P_open)

    melting_pt.append(tm2)



  y = range(len(window))
  return melting_pt,y, close_prob, open_prob

# Salt concentrations in Santa Lucia models usually range from 50 mM to 1 M
