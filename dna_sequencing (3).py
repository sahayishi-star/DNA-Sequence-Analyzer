"""Plus a **SIMPLIFIED MODEL**: DNA Thermal Opening

> Context

DNA "breathes". Its base pairs will constantly open and close, so GC and AT content can impact that.
This breathing is due to the denaturing of DNA via heat. To quantify stability for a simple model, I'll multiply the GC content fraction by 3 (because GC pairs make 3 H-bonds) and AT content fraction by 2 (because AT pairs make 2 H-bonds). Base stacking can also impact denaturation, but for simplicity, I'm assuming only H-bond count impacts DNA breathing for now.
"""

def thermo_stability(dna,n):
  window = [dna[i:i+n] for i in range(0,len(dna) - n+1)]

  thermo_model = []
  open_tend = []

  for w in window:
    G = w.count('G')
    C = w.count('C')
    A = w.count('A')
    T = w.count('T')

    score = (G+C)/len(w)*3 # Standardizing stability based on GC content
    score2 = (A+T)/len(w)*2 # Standardizing stability based on AT content

    thermo_model.append(score+score2)
    inverse = 3-(score+score2) # Flips the relationship
    open_tend.append(inverse)
  open_tend=np.array(open_tend)
  thermo_model=np.array(thermo_model)
  if len(open_tend)>1:
    open_norm = (open_tend-min(open_tend))/(max(open_tend)-min(open_tend)) # min/max normalization
  else:
    open_norm=open_tend

  if len(thermo_model)>1:
    thermo_norm = (thermo_model-min(thermo_model))/(max(thermo_model)-min(thermo_model))
  else:
    thermo_norm=thermo_model
  z = range(len(window))
  return thermo_norm, z, open_norm

